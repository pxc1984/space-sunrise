using Robust.Client.AutoGenerated;
using Robust.Client.UserInterface;
using Robust.Client.UserInterface.Controls;
using Robust.Client.UserInterface.XAML;

namespace Content.Client._Sunrise.CentCom.UI;

[GenerateTypedNameReferences]
public sealed partial class CentComGiftsConsoleEntry : BoxContainer
{
    public string Title;
    public string Description;
    public List<string> Content;

    public CentComGiftsConsoleEntry(string title, string description, List<string> content)
    {
        RobustXamlLoader.Load(this);

        Title = title;
        Description = description;
        Content = content;

        InitFields();
    }

    private void InitFields()
    {
        TitleLabel.Text = Loc.GetString("centcom-gifts-console-name", ("title", Title));
        DescriptionLabel.Text = Loc.GetString("centcom-gifts-console-description", ("desc", Description));
        ContentLabel.Text = Loc.GetString("centcom-gifts-console-content", ("desc", string.Join("\n", Content)));
    }
}

